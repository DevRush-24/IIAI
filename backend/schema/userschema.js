import mongoose from 'mongoose';

const User = new mongoose.Schema({
    name: {
        type: String,
        required:true,
    },
    email: {
        type: String,
        required:true,
    },
    password:{
        type: String,
        required:true,
    }},
    {
        timestamps:true
    }
);

const model= mongoose.model("User",User)

export default model;